import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { discoveries, disciplines, conceptCount, duration, score } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const discoveryList = (discoveries || []).map((d: any) =>
      `• "${d.concept1Text}" (${d.discipline1}) ↔ "${d.concept2Text}" (${d.discipline2}): ${d.insight}`
    ).join('\n');

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          {
            role: "system",
            content: `You are Hermann Hesse reflecting on a completed Glass Bead Game session. Write a contemplative analysis (150-200 words) of the player's journey. Reference their specific discoveries. Comment on the patterns you see in their exploration. End with a thought about what these connections reveal about the unity of knowledge. Write in Hesse's philosophical yet warm style.`
          },
          {
            role: "user",
            content: `Session summary:
- Disciplines explored: ${(disciplines || []).join(', ')}
- Concepts in play: ${conceptCount}
- Duration: ${Math.floor((duration || 0) / 60)} minutes
- Resonance score: ${score || 0}
- Discoveries made:
${discoveryList || 'No synthesis discoveries were made during this session.'}

Write your contemplative analysis of this Glass Bead Game session.`
          }
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited" }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted" }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      throw new Error("AI gateway error");
    }

    const data = await response.json();
    const analysis = data.choices?.[0]?.message?.content || "";

    return new Response(JSON.stringify({ analysis }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });

  } catch (error) {
    console.error("generate-session-analysis error:", error);
    return new Response(JSON.stringify({
      error: error instanceof Error ? error.message : "Unknown error"
    }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
